let rms ?(window : int = 2048) ?(step : int = 512) (x : Audio.audio) =
  let x = Audio.data x in
  let x = Audio.G.slide ~step ~window x in
  let x = Audio.G.abs2 x in
  let x = Audio.G.mean x in
  x
